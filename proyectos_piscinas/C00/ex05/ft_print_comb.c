#include <unistd.h>
#include <stdio.h>

void	ft_print_comb(void);

void	ft_print_comb(void)
{
	char	c[3];
	char	d[2];

	c[0] = '0';
	d[0] = ',';
	d[1] = ' ';
	while (c[0] <= '9')
	{
		c[1] = c[0] + 1;
		while (c[1] <= '9')
		{
			c[2] = c[1] + 1;
			while (c[2] <= '9')
			{
				write(1, &c, 3);
				if (c[0] < '7')
				{
					write(1, &d, 2);
				}
				c[2] += 1;
			}
			c[1] += 1;
		}
		c[0] += 1;
	}
}
