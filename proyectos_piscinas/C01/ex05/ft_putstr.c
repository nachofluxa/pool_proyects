#include <unistd.h>
#include <stdio.h>

void	ft_putstr(char *str);

void	ft_putstr(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		write(1, &str[c], 1);
		c++;
	}
}
